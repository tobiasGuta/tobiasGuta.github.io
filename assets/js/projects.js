/*
  Auto-load public GitHub repositories for projects.html.
  Flow:
  1. Try assets/data/projects.json generated by GitHub Actions.
  2. Fall back to the public GitHub API in the browser.
  3. Render clean project cards without needing manual HTML edits.
*/

const GITHUB_OWNER = 'tobiasGuta';
const STATIC_PROJECTS_URL = 'assets/data/projects.json';
const LIVE_REPOS_URL = `https://api.github.com/users/${GITHUB_OWNER}/repos?sort=updated&per_page=100`;

const grid = document.getElementById('projects-grid');
const count = document.getElementById('projects-count');
const source = document.getElementById('projects-source');

async function fetchJson(url) {
  const response = await fetch(url, { headers: { Accept: 'application/json' } });
  if (!response.ok) {
    throw new Error(`Request failed: ${response.status}`);
  }
  return response.json();
}

function normalizeRepo(repo) {
  return {
    name: repo.name,
    description: repo.description || 'No description yet.',
    html_url: repo.html_url,
    homepage: repo.homepage || '',
    language: repo.language || 'Code',
    topics: Array.isArray(repo.topics) ? repo.topics : [],
    stargazers_count: repo.stargazers_count || 0,
    forks_count: repo.forks_count || 0,
    updated_at: repo.updated_at,
    fork: Boolean(repo.fork),
    archived: Boolean(repo.archived),
  };
}

function sortProjects(projects) {
  return projects
    .filter((repo) => !repo.fork && !repo.archived)
    .sort((a, b) => new Date(b.updated_at) - new Date(a.updated_at));
}

function escapeHtml(value) {
  return String(value ?? '')
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&#039;');
}

function safeExternalUrl(value) {
  try {
    const url = new URL(String(value || ''), window.location.origin);

    if (!['http:', 'https:'].includes(url.protocol)) {
      return '';
    }

    return url.href;
  } catch {
    return '';
  }
}

function formatDate(value) {
  if (!value) return 'unknown';
  return new Intl.DateTimeFormat('en', {
    year: 'numeric',
    month: 'short',
    day: 'numeric',
  }).format(new Date(value));
}

function renderProjects(projects, sourceText) {
  const visibleProjects = sortProjects(projects.map(normalizeRepo));

  count.textContent = `${visibleProjects.length} public projects loaded`;
  source.textContent = sourceText;

  if (!visibleProjects.length) {
    grid.innerHTML = '<div class="project-loading">No public projects found.</div>';
    return;
  }

  grid.innerHTML = visibleProjects.map((repo) => {
    const topics = repo.topics
      .slice(0, 5)
      .map((topic) => `<span>${escapeHtml(topic)}</span>`)
      .join('');
    const repoUrl = repo.html_url ? safeExternalUrl(repo.html_url) : '';
    const homepageUrl = repo.homepage ? safeExternalUrl(repo.homepage) : '';
    const title = repoUrl
      ? `<a href="${escapeHtml(repoUrl)}" class="project-card-title">${escapeHtml(repo.name)}</a>`
      : `<span class="project-card-title">${escapeHtml(repo.name)}</span>`;
    const repoLink = repoUrl
      ? `<a href="${escapeHtml(repoUrl)}" class="project-card-link">Repo</a>`
      : '';
    const homepageLink = homepageUrl
      ? `<a href="${escapeHtml(homepageUrl)}" class="project-card-link">Live</a>`
      : '';

    return `
      <article class="project-card">
        <div class="project-card-topline">
          ${title}
          <span class="project-language">${escapeHtml(repo.language)}</span>
        </div>
        <p>${escapeHtml(repo.description)}</p>
        <div class="project-meta">
          <span>★ ${repo.stargazers_count}</span>
          <span>⑂ ${repo.forks_count}</span>
          <span>Updated ${formatDate(repo.updated_at)}</span>
        </div>
        ${topics ? `<div class="project-topics">${topics}</div>` : ''}
        <div class="project-card-actions">
          ${repoLink}
          ${homepageLink}
        </div>
      </article>
    `;
  }).join('');
}

async function loadProjects() {
  try {
    const staticData = await fetchJson(STATIC_PROJECTS_URL);
    const projects = Array.isArray(staticData) ? staticData : staticData.projects;
    if (!Array.isArray(projects)) {
      throw new Error('Generated project file has no projects array.');
    }
    renderProjects(projects, staticData.generated_at ? `Loaded from generated static data (${formatDate(staticData.generated_at)}).` : 'Loaded from static project data.');
  } catch (staticError) {
    try {
      const liveRepos = await fetchJson(LIVE_REPOS_URL);
      renderProjects(liveRepos, 'Loaded live from the public GitHub API.');
    } catch (liveError) {
      count.textContent = 'Unable to load projects';
      source.textContent = 'Generated data and live GitHub API fallback both failed.';
      grid.innerHTML = '<div class="project-loading">Project feed unavailable right now.</div>';
    }
  }
}

loadProjects();
